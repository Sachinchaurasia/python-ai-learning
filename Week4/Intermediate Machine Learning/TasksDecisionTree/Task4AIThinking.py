##Why might a decision Tree work better than Linear Regression  for  pass fail prediction
#AI Thinking
#1. Non-linearity: Decision trees can capture non-linear relationships between features and the target
#2. Handling Categorical Data: Decision trees can handle categorical features without the need for encoding, while linear regression requires numerical input.
#3. Interpretability: Decision trees are often more interpretable than linear regression models, as they provide a clear visual representation of the decision-making process.
#4. Robustness to Outliers: Decision trees are less sensitive to outliers compared to linear regression, which can be heavily influenced by extreme values.


##A decision Tree is often a better choice because Pass/fail is a categorical outcome,while linear Regression is  designed for predicting continuous numbers

 